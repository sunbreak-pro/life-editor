export const LUCIDE_ICON_NAMES: string[] = Object.keys(LUCIDE_ICON_REGISTRY);

export function parseLucideIconName(
  value: string | null | undefined,
): string | null {
  if (!value) return null;
  if (!value.startsWith(LUCIDE_ICON_PREFIX)) return null;
  const name = value.slice(LUCIDE_ICON_PREFIX.length);
  return name in LUCIDE_ICON_REGISTRY ? name : null;
}

export function formatLucideIconValue(name: string): string {
  return `${LUCIDE_ICON_PREFIX}${name}`;
}
